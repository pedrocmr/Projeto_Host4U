package modelo;


public class Usuario {

	// ATRIBUTOS
	private String login;
	private String senha;
	private String nome;
	private String cpf;
	private String sexo;
	
	

	// CONSTRUTORES
	public Usuario(String login, String senha, String nome,
			String cpf, String sexo) {
		this.login = login;
		this.senha = senha;
		this.nome = nome;
		this.cpf = cpf;
		this.sexo = sexo;
		
	}
	


}
